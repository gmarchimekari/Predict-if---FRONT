public class TestUtilisateur {
    private String id;
    private String nom;
    private String prenom;
    private String mail;

    public TestUtilisateur(String id, String nom, String prenom, String mail) {
        this.id = id;
        this.nom = nom;
        this.prenom = prenom;
        this.mail = mail;
    }

    public String getId() {
        return id;
    }

    public String getNom() {
        return nom;
    }

    public String getPrenom() {
        return prenom;
    }

    public String getMail() {
        return mail;
    }
    
    
    
    
}
